package toString;

/*
oString() 方法可以说是 Java 对象的“自我介绍”或者“身份证”。
简单来说，它的作用就是把一个对象转换成人类可读的字符串。
为了让你彻底明白，我们还是拿刚才的 Student（或者你的 generics_03）类来举例。
1. 为什么要用它？（没有它会怎样？）
在 Java 中，所有的类都继承自 Object 类。如果你不自己写 toString() 方法，Java 就会使用 Object 类里默认的那个。
默认的 toString() 长什么样？
如果你直接打印一个对象，默认输出是这样的：
generics_03@1b6d3586
generics_03：类名。
@：分隔符。
1b6d3586：对象的哈希码（十六进制），你可以把它理解为对象在内存里的“门牌号”。
这就很尴尬了：
你在调试代码时，想看这个学生的名字和分数，结果打印出来是一串乱码似的地址。你根本不知道这个对象里到底存了什么数据。
2. 重写了它会怎样？
当你重写了 toString() 方法（就像你代码里做的那样）：
@Override
public String toString() {
    return this.name + " " + this.score;
}
这时候，你再打印对象，输出就会变成：
张三 90
一目了然！ 这就是 toString() 的核心价值：让对象“说人话”。
3. 它是怎么被调用的？（神奇之处）
toString() 最神奇的地方在于，你通常不需要显式地去调用它，Java 会在很多场景下自动帮你调用。
比如你有这样一个对象：
generics_03 s1 = new generics_03("李四", 100);
下面这三种写法，效果是一模一样的：
写法	代码示例	说明
隐式调用 (最常用)	System.out.println(s1);	打印对象时，Java 自动调用 s1.toString()
拼接调用	String msg = "学生信息：" + s1;	字符串拼接时，自动调用
显式调用	s1.toString();	你自己手动调用（一般不需要）
 */